#include "../minishell.h"

char* filter_v(char *r) 
{
    int i = 0;
    while (r[i] && r[i] != '=')
        i++;
    char *val = malloc(i + 1);
    int j = 0;
    while (j < i)
    {
        val[j] = r[j];
        j++;
    }
    val[j] = '\0';
    return val;
}
