#include "../minishell.h"

char* filter_n(char *r) 
{
    int i = 0;
    while (r[i] && r[i] != '=')
    {
        i++;
    }
    char *val = malloc(i + 1);
    int j = 0;
    while (j < i)
    {
        if(r[j] != '+')
            val[j] = r[j];
        j++;
    }
    return val;
}
